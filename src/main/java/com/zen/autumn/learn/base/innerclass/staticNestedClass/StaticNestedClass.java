package com.zen.autumn.learn.base.innerclass.staticNestedClass;

public class StaticNestedClass {
	
	
	
	public static class PublicStaticNestedClass{
		
		public void function(){
			System.out.println("Public Static Nest Class");
		}
		
		
	}
	
	
	protected static class ProtectedStaticNestedClass{
		
		public  void function(){
		 	System.out.println("Protected Static Nest Class");
			
		}
		
	}
	
	
	private static class PrivateStaticNestedClass{
		
		public  void function(){
		 	System.out.println("Protected Static Nest Class");
			
		}		
		
	}
	
	

}
